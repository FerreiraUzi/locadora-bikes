    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        
        
        int linhaSelecionada = tabelaClientes.getSelectedRow();

    if (linhaSelecionada == -1) {
        JOptionPane.showMessageDialog(this, "Selecione um cliente na tabela!");
        return;
    }
        
                                         
    

    if (linhaSelecionada == -1) {
        JOptionPane.showMessageDialog(this, "Selecione um cliente para editar.");
        return;
    }

    Cliente c = new Cliente();
    // Supondo que o ID está na coluna 0 da tabela (você precisa do ID para atualizar)
    int id = Integer.parseInt(tabelaClientes.getValueAt(linhaSelecionada, 0).toString());
    c.setId(id);
    c.setNome(txtNome.getText());
    c.setCpf(txtCpf.getText());
    c.setTelefone(txtTelefone.getText());
    c.setEmail(txtEmail.getText());

    ClienteDAO dao = new ClienteDAO();
    dao.update(c);  // método update que você deve ter no DAO

    listarClientes(); // atualiza a tabela com os dados do banco
    limparCampos();   // método para limpar os campos após atualizar
    
    }//GEN-LAST:event_btnEditarActionPerformed

    private void preencherCamposParaEditar() {
    int linhaSelecionada = tabelaClientes.getSelectedRow();
    if (linhaSelecionada == -1) {
        JOptionPane.showMessageDialog(this, "Selecione um cliente para editar.");
        return;
    }

    // Pega os valores da tabela (ajuste os índices das colunas conforme sua tabela)
    String nome = tabelaClientes.getValueAt(linhaSelecionada, 1).toString();
    String cpf = tabelaClientes.getValueAt(linhaSelecionada, 2).toString();
    String telefone = tabelaClientes.getValueAt(linhaSelecionada, 3).toString();
    String email = tabelaClientes.getValueAt(linhaSelecionada, 4).toString();

    txtNome.setText(nome);
    txtCpf.setText(cpf);
    txtTelefone.setText(telefone);
    txtEmail.setText(email);
}

    
    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        
                                            
 
    int linhaSelecionada = tabelaClientes.getSelectedRow();
    if (linhaSelecionada == -1) {
        JOptionPane.showMessageDialog(this, "Selecione um cliente na tabela!");
        return;
    }

    int id = Integer.parseInt(tabelaClientes.getValueAt(linhaSelecionada, 0).toString());
    Cliente cliente = new Cliente();
    cliente.setId(id);

    ClienteDAO dao = new ClienteDAO();
    dao.delete(cliente);

    listarClientes(); // Atualiza a tabela
    limparCampos();   // Limpa os campos

    }//GEN-LAST:event_btnExcluirActionPerformed

